package ir.ramtung.tinyme.domain.entity;

public class CustomPair {
    private int first;
    private int second;

    public CustomPair(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}